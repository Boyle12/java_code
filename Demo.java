public class Demo {
    public static void main(String args[]){
        int a = 10;
        int b = 5;
        System.out.println("a");
        // when we use double inverted commas("")then print itself
        System.out.println(a);
        // when we use double inverted commas("")then print the values
        System.out.println("b");
        System.out.println(b);
    }
    
}
